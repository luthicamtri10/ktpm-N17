    public void formInit() {
        JFreeChart pieChart = createChart(createDataset(Redux.statisticsBUS.getEmployeeGender()));
        ChartPanel chrtPanel = new ChartPanel(pieChart);
        // Set layout manager for chartPanel
        jPanel3.setLayout(new BorderLayout());
        // Add chrtPanel to chartPanel with BorderLayout.CENTER constraint
        jPanel3.add(chrtPanel, BorderLayout.CENTER);

        jLabel1.setText("Tổng số nhân viên: " + Redux.statisticsBUS.getEmployeeGender().get(4));
        jLabel2.setText("Tổng số nhân viên nam: " + Redux.statisticsBUS.getEmployeeGender().get(0));
        jLabel3.setText("Tỷ lệ nhân viên nam: " + df.format(Redux.statisticsBUS.getEmployeeGender().get(1))
                + "%");
        jLabel4.setText("Tổng số nhân viên nữ: " + Redux.statisticsBUS.getEmployeeGender().get(2));
        jLabel5.setText("Tỷ lệ nhân viên nữ: " + df.format(Redux.statisticsBUS.getEmployeeGender().get(3))
                + "%");
    }

    private JFreeChart createChart(PieDataset dataset) {
        JFreeChart chart = ChartFactory.createPieChart(
                "BIỂU ĐỒ THỐNG KÊ TỶ LỆ GIỚI TÍNH CỦA NHÂN VIÊN TẠI CÔNG TY", dataset,
                true, true, true);
        return chart;
    }

    private PieDataset<String> createDataset(ArrayList<Object> listGender) {
        DefaultPieDataset<String> dataset = new DefaultPieDataset<>();
        dataset.setValue("Nhân Viên Nam: " + df.format(Redux.statisticsBUS.getEmployeeGender().get(1))
                + "%", (Double) listGender.get(1));
        dataset.setValue("Nhân Viên Nữ: " + df.format(Redux.statisticsBUS.getEmployeeGender().get(3))
                + "%", (Double) listGender.get(3));
        return dataset;
    }

