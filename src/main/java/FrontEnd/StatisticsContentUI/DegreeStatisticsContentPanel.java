    public void formInit() {
        JFreeChart pieChart = createChart(createDataset(Redux.statisticsBUS.getEmployeeDegree()));
        ChartPanel chrtPanel = new ChartPanel(pieChart);
        // Set layout manager for chartPanel
        jPanel3.setLayout(new BorderLayout());
        // Add chrtPanel to chartPanel with BorderLayout.CENTER constraint
        jPanel3.add(chrtPanel, BorderLayout.CENTER);

        ArrayList<Object> listDegree = Redux.statisticsBUS.getEmployeeDegree();

        for (int i = 0; i < listDegree.size() - 1; i++) {
            ArrayList<Object> col = (ArrayList<Object>) listDegree.get(i);
            String degreeName = (String) col.get(0);
            int count = (int) col.get(1);
            JLabel label = switch (i) {
                case 0 ->
                    jLabel1;
                case 1 ->
                    jLabel2;
                case 2 ->
                    jLabel3;
                case 3 ->
                    jLabel4;
                case 4 ->
                    jLabel5;
                case 5 ->
                    jLabel6;
                case 6 ->
                    jLabel7;
                default ->
                    null;
            };
            if (label != null) {
                label.setText((i + 1) + ") " + degreeName + ": " + count);
            }
        }
    }

    private JFreeChart createChart(PieDataset<String> dataset) {
        JFreeChart chart = ChartFactory.createPieChart(
                "BIỂU ĐỒ THỐNG KÊ TỶ LỆ BẰNG CẤP CỦA NHÂN VIÊN TẠI CÔNG TY", dataset,
                true, true, true);
        return chart;
    }

    private PieDataset<String> createDataset(ArrayList<Object> listGender) {
        DefaultPieDataset<String> dataset = new DefaultPieDataset<>();

        for (int i = 0; i < listGender.size() - 1; i++) {
            ArrayList<Object> subList = (ArrayList<Object>) listGender.get(i);
            double value = ((Integer) subList.get(1)).doubleValue();
            double total = ((Integer) listGender.get(listGender.size() - 1)).doubleValue();
            dataset.setValue((String) subList.get(0) + ": "
                    + df.format(value * 100.0 / total)
                    + "%", value * 100.0 / total);
        }
        return dataset;
    }

